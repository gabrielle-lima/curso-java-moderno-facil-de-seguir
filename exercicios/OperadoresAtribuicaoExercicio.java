public class OperadoresAtribuicaoExercicio {
    public static void main(String[] args)  {
        int numero = 10;
        System.out.println(numero);
        numero += 3; // numero = numero + 3 // 13
        System.out.println(numero);
        numero -= 2; // numero = numero - 2 // 13 - 2 = 11
        System.out.println(numero);
        numero *= 4; // numero = numero * 4 // 11 * 4 = 44
        System.out.println(numero);
        numero /= 2; // numero = numero / 2 //  44 / 2 = 22
        System.out.println(numero);
    }

}
