public class PromocaoAutomatica {
    public static void main(String[] args) {
        byte a = 1;
        short b = 10;
        char c = 'G';
        int d = 34;
        long e = 13L;
        float f = 67.8f;
        double g = 89.00;


       int  operacaoInt = a + b;

       int resultado = a * d;

       float operacao = e - f;


       double calculo = f / g;

        System.out.println("Resultado da operacao:  " + operacaoInt);
        System.out.println("Resultado da operacao:  " + resultado);
        System.out.println("Resultado da operacao:  " + operacao);
        System.out.println("Resultado da operacao:  " + calculo);
        System.out.println("Valor do char:  " + c);





    }
}
