import java.util.concurrent.atomic.DoubleAccumulator;

public class PrecedenciaOperadoresAtribuicao {
  public static void main(String[] args) {
        int numero = 5;
        ++numero;
        System.out.println(numero);
        --numero;
        System.out.println(numero);
        numero++;
        System.out.println(numero);
        numero--;
        System.out.println(numero);

    }
}
