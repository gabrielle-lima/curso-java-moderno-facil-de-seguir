public class operadoresDeComparacao {
	public static void main(String[] args) {
			int a = 10;
			int b = 20;
			System.out.println(a == b);
			// Saída: false
			System.out.println(a != b);
			// Saída: true
			System.out.println(a > b);
			// Saída: false
			System.out.println(a < b);
			// Saída: true
			System.out.println(a >= b);
			// Saída: false
			System.out.println(a <= b);
			// Saída: true
					
		       }

		   }	