	public class operadoresAritmeticos {
			public static void main(String[] args) {
			int a = 10;
            int b = 2;


			System.out.println(a + b);
			// Saída: 12
			System.out.println(a - b);
			// Saída: 8
			System.out.println(a * b);
			// Saída: 20
			System.out.println(a / b);
			// Saída: 5
			System.out.println(a % b);
			// Saída: 0
					
		       }

		   }	