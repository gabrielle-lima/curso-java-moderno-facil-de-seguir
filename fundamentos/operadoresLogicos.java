public class operadoresLogicos {
	public static void main(String[] args) {
			int a = 10;
			int b = 20;
			boolean c = true; 
			System.out.println(a < b && c);
			// Saída: true
			System.out.println(a > b || c);
			// Saída: true
			System.out.println(!c);
			// Saída: false
					
		       }

		   }	